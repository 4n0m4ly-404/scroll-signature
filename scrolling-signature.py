import time
import os
import sys

def center_text(text, width):
    lines = text.strip().split('\n')
    centered_lines = [(line.center(width)) for line in lines]
    return centered_lines

def scroll_text(text, delay=0.05):
    # Get the width and height of the terminal
    width = os.get_terminal_size().columns
    height = os.get_terminal_size().lines

    # Center the text to fit the terminal width
    text_lines = center_text(text, width)

    # Pad the text to fit the terminal height
    if len(text_lines) < height:
        text_lines += [' ' * width] * (height - len(text_lines))

    # Create a list of lines to be scrolled
    scroll_lines = text_lines + [' ' * width] * height

    while True:
        for i in range(len(scroll_lines) - height + 1):
            sys.stdout.write('\x1b[H')  # Move cursor to top-left corner
            # Print the current viewport of the text
            for line in scroll_lines[i:i + height]:
                sys.stdout.write(line + '\n')
            sys.stdout.flush()
            time.sleep(delay)

